var opsys = process.platform;
const { exec } = require("child_process");

if(opsys === "linux") {
    console.log(`Your os is ${opsys}`);

    exec("./patch.sh", (error, stdout, stderr) => {
        console.log(`stdout: ${stdout}`);
    });

} else if(opsys == "Windows") {
    console.log(`Your os is ${opsys}`);

    exec("./patch.sh", (error, stdout, stderr) => {
        console.log(`stdout: ${stdout}`);
    });
}